package com.stratego.model;

/**
 * Point --- Class that defines a Point object, code taken from the second practical class.
 * @author 
 */

public class Point {
    
	public int x, y;

	// Default constructor
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //Accessors
    public int getX() {
      return x;
    }

    public int getY() {
      return y;
    }
  
}
